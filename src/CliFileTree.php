    /**
     * The name and signature of the console command.
     *
     * @var string
     */
    protected $signature = 'clifiletree
                           {--filter=}
                           {--fg=}
                           {--bg=}';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'Helps selecting a file using a menu interface with navigation ðŸŽ‰

  --filter : (optional) set "folder" only or filter files by extensions. Supports multiple file extension filters by seperating with comma (Ex: csv,xls)
  --fg : (optional) foreground color (def. green)
  --bg : (optional) background color (def. black)

  (possible color values : black, red, green, yellow, blue, magenta, cyan, white)
  ';

    /**
     * Menu object
     */
    private $menu = '';

    /**
     * Final selected item
     */
    private $selectedItem;

    /**
     * Current directory path
     */
    private $currentdir = '';

    /**
     * List of all files and directories under current dir
     */
    private $filelist = array();

    /**
     * Total number of files and directories under current dir.
     */
    private $filecount = 0;

    /**
     * List of files and directories in current paginated view
     */
    private $current_filelist = array();

    /**
     * Current pagination index (int)
     */
    private $pagi = 0;

    /**
     * Create a new command instance.
     *
     * @return void
     */
    public function __construct()
    {
        parent::__construct();
    }

    /**
     * Execute the console command.
     *
     * @return mixed
     */
    public function handle()
    {
        /* show file selector menu */
        $this->startFileMenu();
    }

    /**
    * CLI File Tree Menu builder
    *
    */
    public function startFileMenu()
    {
        $this->selectedItem = '';

        $this->currentdir = $this->get_home_dir();

        // build our menu
        $menu = (new CliMenuBuilder);

        $file_types = '';
        if ($this->option('filter') != '') {
            if ($this->option('filter') == 'folder') {
                $title = 'Select a folder';
            } else {
                $file_types = (strpos($this->option('filter'), ',')) ? str_replace(",", " OR ", $this->option('filter')) : $this->option('filter');
                $title = 'Select a ' . $file_types . ' file';
            }
        }

        $fg = ($this->option('fg') == '') ? 'green' : $this->option('fg');
        $bg = ($this->option('bg') == '') ? 'black' : $this->option('bg');

        $menu->setTitle($title)
    ->setTitleSeparator('')
    ->setForegroundColour($fg)
    ->setBackgroundColour($bg)
    ->addStaticItem('Current directory : ' . $this->currentdir)
    ->addLineBreak('=')
    ->addLineBreak('');

        $this->filelist = $this->buildFileTree();
        $this->current_filelist = $this->filelist[$this->pagi];

        foreach ($this->current_filelist as $item) {
            $menu->addItem($item, array($this, 'selectItem'));
        }

        /* prepare for navigation */
        $menu->addLineBreak('');

        if ($this->filecount > 10) {
            $menu->addStaticItem('Navigation')
      ->addLineBreak('=');

            // add navigation items
            if ($this->pagi > 0) {
                $menu->addItem('Previous', array($this, 'pagiPrev'));
            }

            if (($this->pagi+1) < count($this->filelist)) {
                $menu->addItem('Next', array($this, 'pagiNext'));
            }

            $menu->addLineBreak('=')->addLineBreak('');
        }

        $this->menu = $menu->build();

        /* show time! */
        $this->menu->open();

        if ($this->selectedItem != '') {
            $this->line($this->selectedItem);
        }
    }

    /**
    * CLI File Tree Navigation Action
    *
    */
    private function menuNavigate($target = '')
    {
        if ($target != '') {
            if ($target == '..') {
                $this->currentdir = substr($this->currentdir, 0, strrpos($this->currentdir, "/"));
                $this->currentdir = ($this->currentdir == '') ? "/" : $this->currentdir;
            } else {
                $currentdir = ($this->currentdir == '/') ? '' : $this->currentdir;
                $this->currentdir = $currentdir . '/' . $target;
            }

            $this->filelist = $this->buildFileTree();
            $this->pagi = 0;
            $this->current_filelist = $this->filelist[$this->pagi];
        }

        foreach ($this->menu->getItems() as $item) {
            $this->menu->removeItem($item);
        }

        /* rebuild header */
        $this->menu->addItem(new StaticItem('Current directory : ' . $this->currentdir));
        $this->menu->addItem(new LineBreakItem('='));
        $this->menu->addItem(new LineBreakItem(''));

        if (!in_array("[DIR] ..", $this->current_filelist)) {
            $this->menu->addItem(new SelectableItem("[DIR] ..", array( $this, 'selectItem' )));
        }

        foreach ($this->current_filelist as $item) {
            $this->menu->addItem(new SelectableItem($item, array( $this, 'selectItem' )));
        }

        $this->menu->addItem(new LineBreakItem(''));

        /* rebuild footer */
        if ($this->filecount > 10) {
            $this->menu->addItem(new LineBreakItem(''));
            $this->menu->addItem(new StaticItem('Navigation'));
            $this->menu->addItem(new LineBreakItem('='));

            if ($this->pagi > 0) {
                $this->menu->addItem(new SelectableItem('Previous', array( $this, 'pagiPrev' )));
            }

            if (($this->pagi+1) < count($this->filelist)) {
                $this->menu->addItem(new SelectableItem('Next', array($this, 'pagiNext' )));
            }
        }

        $this->menu->addItem(new LineBreakItem('='));

        $this->menu->addItem(new LineBreakItem(''));
        $this->menu->addItem(new SelectableItem('Exit', array($this, 'exitMenu' )));

        $this->menu->redraw(false);
    }

    /**
    * CLI File Tree menu item select callback
    *
    */
    public function selectItem(CliMenu $menu)
    {
        $target = str_replace("[DIR] ", "", $menu->getSelectedItem()->getText());

        if ($target == ".") {
            $this->selectedItem = $this->currentdir;
            $this->menu->close();
        } elseif ($target == "..") {
            $this->menuNavigate($target);
        } else {
            if (@is_dir($this->currentdir . '/' . $target)) {
                $this->menuNavigate($target);
            } else {
                $this->selectedItem = $this->currentdir . '/' . $menu->getSelectedItem()->getText();
                $this->menu->close();
            }
        }
    }

    /**
    * CLI File Tree Data
    *
    */
    private function buildFileTree()
    {
        $files = scandir($this->currentdir);

        $dirlist = [];
        $filelist = [];

        foreach ($files as $file) {
            $item = $this->currentdir . '/' . $file;

            if (!is_readable($item)) {
                continue;
            }
            if (!file_exists($item)) {
                continue;
            }

            // directory?
            if (is_dir($item)) {
                $dirlist[] = '[DIR] ' . $file;
            } else {
                // folder filter?
                if ($this->option('filter') == 'folder') {
                    continue;
                }

                // file extension?
                if ($this->option('filter') != '') {
                    $ext = substr($file, strrpos($file, '.') + 1);
                    $file_types = (strpos($this->option('filter'), ',')) ? explode(",", $this->option('filter')) : array( $this->option('filter') );

                    if (in_array($ext, $file_types)) {
                        $filelist[] = $file;
                    }
                } else {
                    $filelist[] = $file;
                }
            }
        }

        $list = array_merge($dirlist, $filelist);

        $this->filecount = count($list);

        return array_chunk($list, 10, true);
    }

    /**
    * CLI File Tree pagination, previous action
    *
    */
    public function pagiPrev()
    {
        $this->setPagi(($this->pagi-1));
        $this->current_filelist = $this->filelist[$this->pagi];
        $this->menuNavigate();
    }

    /**
    * CLI File Tree pagination, next action
    *
    */
    public function pagiNext()
    {
        $this->setPagi(($this->pagi+1));
        $this->current_filelist = $this->filelist[$this->pagi];
        $this->menuNavigate();
    }

    /**
    * CLI File Tree pagination, set pagination index
    *
    */
    private function setPagi($pagi)
    {
        return $this->pagi = $pagi;
    }

    /**
    * CLI File Tree exit menu helper
    *
    */
    public function exitMenu(CliMenu $menu)
    {
        $menu->closeThis();
    }

    /**
    * CLI File Tree get home directory
    *
    */
    private function get_home_dir()
    {
        $home = getenv('HOME');
        if (!empty($home)) {
            $home = rtrim($home, '/');
        } elseif (!empty($_SERVER['HOMEDRIVE']) && !empty($_SERVER['HOMEPATH'])) {
            $home = $_SERVER['HOMEDRIVE'] . $_SERVER['HOMEPATH'];
            $home = rtrim($home, '\\/');
        }
        return empty($home) ? null : $home;
    }

